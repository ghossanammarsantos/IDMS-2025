     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        // Mengambil data customer dan jumlah container per customer
        $reportsCustomer = DB::table('surveyin')
        ->join('ann_import', 'ann_import.no_container', '=', 'surveyin.no_container')
        ->select('ann_import.consignee', DB::raw('COUNT(DISTINCT surveyin.no_container) as jumlah_container'))
        ->groupBy('ann_import.consignee')
        ->get();


        return view('admin.reportcustomer.index', compact('reportsCustomer'));

    }

